//switch case- in switch case the concept is same as key and suitcase.
//key matched then case is opend.in switch case we use break after case sattement 
// if we do not use break then execute of matched cased block  then run next case block.

#include<stdio.h>
int main()
{
	switch(4)
	{
		case 1:
			printf("Hello1");
			break;
		case 2:
			printf("Hello 2");
			break;
		case 3:
			printf("Hello 3");
			break;
		case 4:
			printf("Hello 4");
			break;
	}
}

